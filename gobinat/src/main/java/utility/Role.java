package utility;

public class Role {
	private String roleName;
	private boolean isRMR;

	public String getRoleName() {
		return roleName;
	}

	public void setRoleName(String roleName) {
		this.roleName = roleName;
	}

	public boolean isRMR() {
		return isRMR;
	}

	public void setRMR(boolean isRMR) {
		this.isRMR = isRMR;
	}
}
